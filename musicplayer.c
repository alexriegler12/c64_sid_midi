/* -------------------------------------------------------------------------
   TIMING STATE
   ------------------------------------------------------------------------- */
   
unsigned long mpqn = 500000; 
unsigned int ppqn = 96;      
volatile unsigned long master_tick = 0; 

/* Interrupt Handler (Called by IRQ) */
unsigned char irq_handler2(void) {
    /* Check if Timer A generated the interrupt */
    if (CIA1_ICR & 0x01) {
        master_tick += TICKS_PER_IRQ;
        return IRQ_HANDLED;
    }
    return IRQ_NOT_HANDLED;
}

void recalc_timing() {
    /* 
       Calculate Timer Latch.
       CyclesPerTick = MPQN / PPQN (approx 1us/cycle).
       Latch = CyclesPerTick * TICKS_PER_IRQ.
    */
    unsigned long cycles_per_tick = mpqn / ppqn;
    unsigned long total_cycles = cycles_per_tick * TICKS_PER_IRQ;
    unsigned int latch;

    /* Safety clamp for very fast tempos/high PPQN to prevent IRQ flooding */
    if (total_cycles < 2000) total_cycles = 2000; 
    if (total_cycles > 65535L) total_cycles = 65535L;
    
    latch = (unsigned int)total_cycles;

    /* Update hardware registers atomically */
    __asm__("sei");
    CIA1_TA_LO = latch & 0xFF;
    CIA1_TA_HI = latch >> 8;
    __asm__("cli");
}

/* Helper to read volatile long safely */
unsigned long get_master_tick() {
    unsigned long t;
    __asm__("sei");
    t = master_tick;
    __asm__("cli");
    return t;
}

/* -------------------------------------------------------------------------
   VOICE MANAGEMENT
   ------------------------------------------------------------------------- */

#define NUM_SID_VOICES 3
#define MAX_TRACKS 20

typedef struct {
    unsigned char note;       
    unsigned char channel;    
    unsigned char active;     
    unsigned long age;        /* Birth Timestamp */
    unsigned char waveform;   
} VoiceState;

VoiceState voices[NUM_SID_VOICES];

typedef struct {
    const unsigned char* ptr;
    unsigned char running_status;
    unsigned long absolute_event_time; 
    unsigned char finished;
} Track;

Track tracks[MAX_TRACKS];
unsigned char num_tracks = 0;
unsigned char channel_instrument[16];

void sid_init() {
    unsigned char i;
    SID.amp = 0x0F; 
    SID.flt_freq = 0;
    
    /* Reset Timer State */
    __asm__("sei");
    master_tick = 0;
    mpqn = 500000;
    CIA1_CRA = 0x00; /* Stop Timer A */
    CIA1_ICR = 0x7F; /* Disable all CIA interrupts */
    (void)CIA1_ICR;  /* Ack pending */
    __asm__("cli");

    for(i=0; i<NUM_SID_VOICES; ++i) {
        voices[i].active = 0;
        voices[i].age = 0;
        voices[i].note = 0;
        voices[i].channel = 255;
        voices[i].waveform = 0;
    }
    
    SID.v1.ctrl = 0;
    SID.v2.ctrl = 0;
    SID.v3.ctrl = 0;
}

void hw_note_on(unsigned char v_idx, int freq, unsigned char wave, unsigned char ad, unsigned char sr, int pulse) {
    voices[v_idx].waveform = wave;
    switch(v_idx) {
        case 0:
            SID_STOP(v1, wave); 
            SID_AD(v1, ad); SID_SR(v1, sr);
            if(wave & SID_PULSE_W) SID_PULSE(v1, pulse);
            SID_FREQ(v1, freq); SID_START(v1, wave);
            break;
        case 1:
            SID_STOP(v2, wave);
            SID_AD(v2, ad); SID_SR(v2, sr);
            if(wave & SID_PULSE_W) SID_PULSE(v2, pulse);
            SID_FREQ(v2, freq); SID_START(v2, wave);
            break;
        case 2:
            SID_STOP(v3, wave);
            SID_AD(v3, ad); SID_SR(v3, sr);
            if(wave & SID_PULSE_W) SID_PULSE(v3, pulse);
            SID_FREQ(v3, freq); SID_START(v3, wave);
            break;
    }
}

void hw_note_off(unsigned char v_idx) {
    unsigned char w = voices[v_idx].waveform;
    switch(v_idx) {
        case 0: SID_STOP(v1, w); break;
        case 1: SID_STOP(v2, w); break;
        case 2: SID_STOP(v3, w); break;
    }
}

/* -------------------------------------------------------------------------
   INSTRUMENT MAPPING
   ------------------------------------------------------------------------- */

void trigger_drum(unsigned char v_idx, unsigned char note) {
    unsigned char wf = SID_NOISE;
    unsigned char ad = 0x05; 
    unsigned char sr = 0x00;
    int freq = 2000;

    if (note == 35 || note == 36) { 
        wf = SID_TRI; freq = 300; ad = 0x08; 
    } else if (note == 42 || note == 44 || note == 46) { 
        freq = 8000;
    } else if (note == 49 || note == 57) { 
        freq = 6000; ad = 0x08; sr = 0x55;
    }
    hw_note_on(v_idx, freq, wf, ad, sr, 0);
}

void trigger_instrument(unsigned char v_idx, unsigned char note, unsigned char prog) {
    unsigned char wf;
    unsigned char ad, sr;
    int pulse = 0x800;
    int freq;
    int table_idx = note - 12; 

    if (table_idx < 0) table_idx = 0;
    else if (table_idx > 95) table_idx = 95;
    freq = note_table_pal[table_idx];

    if (prog < 8) { wf = SID_PULSE_W; pulse = 0x400; ad = 0x09; sr = 0x00; }
    else if (prog < 16) { wf = SID_TRI; ad = 0x09; sr = 0x00; }
    else if (prog < 24) { wf = SID_TRI; ad = 0x00; sr = 0xF0; }
    else if (prog < 32) { wf = SID_SAW; ad = 0x09; sr = 0x58; }
    else if (prog < 40) { wf = SID_PULSE_W; pulse = 0x200; ad = 0x19; sr = 0x95; }
    else if (prog < 56) { wf = SID_SAW; ad = 0x59; sr = 0xA8; }
    else { wf = SID_PULSE_W; pulse = 0x600; ad = 0x35; sr = 0xF5; }

    hw_note_on(v_idx, freq, wf, ad, sr, pulse);
}

/* -------------------------------------------------------------------------
   ALLOCATOR
   ------------------------------------------------------------------------- */

void play_note(unsigned char channel, unsigned char note, unsigned char velocity) {
    unsigned char v_idx = 255;
    register unsigned char i;
    unsigned long min_timestamp = 0xFFFFFFFF;
    unsigned long cur_time;
    //if(channel==15) return;
    for(i=0; i<NUM_SID_VOICES; ++i) {
        if(voices[i].active && voices[i].channel == channel && voices[i].note == note) {
            v_idx = i; break;
        }
    }

    if(v_idx == 255) {
        for(i=0; i<NUM_SID_VOICES; ++i) {
            if(!voices[i].active) { v_idx = i; break; }
        }
    }

    /* Reverse Age Stealing (Lowest Timestamp = Oldest) */
    if(v_idx == 255) {
        for(i=0; i<NUM_SID_VOICES; ++i) {
            if(voices[i].age < min_timestamp) {
                min_timestamp = voices[i].age;
                v_idx = i;
            }
        }
    }

    /* Get atomic time for birthdate */
    cur_time = get_master_tick();

    voices[v_idx].active = 1;
    voices[v_idx].note = note;
    voices[v_idx].channel = channel;
    voices[v_idx].age = cur_time;

    if (channel == 9) trigger_drum(v_idx, note);
    else trigger_instrument(v_idx, note, channel_instrument[channel]);
    
    (void)velocity;
}

void stop_note(unsigned char channel, unsigned char note) {
    register unsigned char i;
    for(i=0; i<NUM_SID_VOICES; ++i) {
        if(voices[i].active && voices[i].channel == channel && voices[i].note == note) {
            hw_note_off(i); 
            voices[i].active = 0; 
            return;
        }
    }
}

