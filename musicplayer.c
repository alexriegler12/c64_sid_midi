#define SID_START(v, w) SID.v.ctrl = (w) | 0x01
#define SID_STOP(v, w)  SID.v.ctrl = (w)
#define SID_FREQ(v, f)  SID.v.freq = (f)
#define SID_AD(v, val)  SID.v.ad = (val)
#define SID_SR(v, val)  SID.v.sr = (val)
#define SID_PULSE(v, p) SID.v.pw = (p)

#define SID_TRI     0x10
#define SID_SAW     0x20
#define SID_PULSE_W 0x40
#define SID_NOISE   0x80

/* CIA1 Registers for Timer A */
#define CIA1_TA_LO (*(volatile unsigned char*)0xDC04)
#define CIA1_TA_HI (*(volatile unsigned char*)0xDC05)
#define CIA1_CRA   (*(volatile unsigned char*)0xDC0E)
#define CIA1_ICR   (*(volatile unsigned char*)0xDC0D)

/* Granularity: Number of MIDI ticks per Interrupt */
#define TICKS_PER_IRQ 4 

