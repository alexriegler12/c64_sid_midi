unsigned long read_var_len(unsigned char track_id) {
    unsigned long value = 0;
    register unsigned char c;
    do {
        c = *tracks[track_id].ptr++;
        value = (value << 7) | (c & 0x7F);
    } while (c & 0x80);
    return value;
}

void play_midi() {
    const unsigned char* base_ptr = midi_file;
    unsigned int format;
    register unsigned char i;
    unsigned long chunk_len;
    unsigned char tracks_running;
    unsigned long current_tick;
    
    if (base_ptr[0] != 'M' || base_ptr[1] != 'T') return;
    base_ptr += 8; 
    
    format = (base_ptr[0] << 8) | base_ptr[1]; base_ptr += 2;
    num_tracks = (base_ptr[0] << 8) | base_ptr[1]; base_ptr += 2;
    ppqn = (base_ptr[0] << 8) | base_ptr[1]; base_ptr += 2; 

    if (num_tracks > MAX_TRACKS) num_tracks = MAX_TRACKS;

    for(i=0; i<num_tracks; ++i) {
        while(!(base_ptr[0] == 'M' && base_ptr[1] == 'T' && base_ptr[2] == 'r' && base_ptr[3] == 'k')) {
            base_ptr++; 
        }
        base_ptr += 4; 
        
        chunk_len = ((unsigned long)base_ptr[0] << 24) | ((unsigned long)base_ptr[1] << 16) |
                    ((unsigned long)base_ptr[2] << 8) | base_ptr[3];
        base_ptr += 4;

        tracks[i].ptr = base_ptr;
        tracks[i].running_status = 0;
        tracks[i].finished = 0;
        tracks[i].absolute_event_time = read_var_len(i);
        
        base_ptr += chunk_len;
    }

    for(i=0; i<16; ++i) channel_instrument[i] = 0;
    sid_init();
    
    /* Setup IRQ */
    recalc_timing();
    set_irq(&irq_handler2, NULL, 0);
    
    /* Enable CIA1 Timer A Interrupts */
    __asm__("sei");
    CIA1_ICR = 0x81; /* Enable Timer A (bit 0 + set bit 7) */
    CIA1_CRA = 0x11; /* Load Latch, Start Timer, Continuous Mode */
    __asm__("cli");

    while(1) {
        tracks_running = 0;
        
        /* 1. Poll Atomic Clock */
        current_tick = get_master_tick();

        /* 2. Check all tracks */
        for(i=0; i<num_tracks; ++i) {
            if (tracks[i].finished) continue;
            
            tracks_running = 1;

            /* Check if track is ready to fire */
            if (!tracks[i].finished && current_tick >= tracks[i].absolute_event_time) {
                
                unsigned char status = *tracks[i].ptr++;
                
                if (status == 0xFF) {
                    unsigned char type = *tracks[i].ptr++;
                    unsigned long len = read_var_len(i);
                    
                    if (type == 0x2F) {
                        tracks[i].finished = 1;
                    } 
                    else if (type == 0x51 && len == 3) {
                        unsigned char t1 = *tracks[i].ptr++;
                        unsigned char t2 = *tracks[i].ptr++;
                        unsigned char t3 = *tracks[i].ptr++;
                        mpqn = ((unsigned long)t1 << 16) | ((unsigned int)t2 << 8) | t3;
                        recalc_timing();
                    }
                    else {
                        tracks[i].ptr += len;
                    }
                }
                else {
                    unsigned char cmd, ch, note, vel;
                    
                    if (status < 0x80) {
                        status = tracks[i].running_status;
                        tracks[i].ptr--;
                    } else {
                        tracks[i].running_status = status;
                    }

                    cmd = status & 0xF0;
                    ch = status & 0x0F;

                    if (cmd == 0x90) {
                        note = *tracks[i].ptr++;
                        vel = *tracks[i].ptr++;
                        if (vel > 0) play_note(ch, note, vel);
                        else stop_note(ch, note);
                    }
                    else if (cmd == 0x80) {
                        note = *tracks[i].ptr++;
                        vel = *tracks[i].ptr++;
                        stop_note(ch, note);
                    }
                    else if (cmd == 0xC0) {
                        channel_instrument[ch] = *tracks[i].ptr++;
                    }
                    else if (cmd == 0xB0 || cmd == 0xE0) {
                        tracks[i].ptr++; tracks[i].ptr++;
                    }
                    else {
                        tracks[i].ptr++;
                    }
                }

                if (!tracks[i].finished) {
                    tracks[i].absolute_event_time += read_var_len(i);
                }
            }
        }

        if (!tracks_running) break; 
    }
    
    /* Cleanup IRQ */
    __asm__("sei");
    CIA1_CRA = 0x00;
    CIA1_ICR = 0x7F;
    __asm__("cli");
}

